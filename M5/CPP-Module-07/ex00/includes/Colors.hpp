#ifndef COLORS_HPP
#define COLORS_HPP

// Reset
#define RESET   "\033[0m"

// Text styles
#define BOLD    "\033[1m"
#define DIM     "\033[2m"
#define ITALIC  "\033[3m"
#define UNDER   "\033[4m"

// Basic colors
#define BLACK   "\033[30m"
#define RED     "\033[31m"
#define GREEN   "\033[32m"
#define YELLOW  "\033[33m"
#define BLUE    "\033[34m"
#define MAGENTA "\033[35m"
#define CYAN    "\033[36m"
#define WHITE   "\033[37m"

// Bright colors (256-color palette)
#define BRIGHT_RED     "\033[38;5;196m"
#define BRIGHT_GREEN   "\033[38;5;46m"
#define BRIGHT_YELLOW  "\033[38;5;226m"
#define BRIGHT_BLUE    "\033[38;5;51m"
#define BRIGHT_MAGENTA "\033[38;5;201m"
#define BRIGHT_CYAN    "\033[38;5;51m"
#define ORANGE         "\033[38;5;208m"
#define PURPLE         "\033[38;5;141m"
#define PINK           "\033[38;5;213m"

#endif
